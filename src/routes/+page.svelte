	<p>Skoro zajrzałeś/aś tutaj, to znaczy, że:</p>

	<ul>
		<li>siedzisz w biurze (przepraszam, pracujesz) i/lub</li>
		<li>
			jesteś ciekaw/a, czy kodowanie/programowanie może być równie dobrym wypełniaczem Twojego mniej
			produktywnego czasu w pracy jak, np. skrolowanie Facebooka, Instagrama, portali informacyjnych
			czy sklepów internetowych lub też pisanie wiadomości/ rozmawianie przez telefon.
		</li>
	</ul>

	<p>
		Tworzę tę stronę w celach edukacyjnych (lubię programować, dzielić się swoją wiedzą oraz zarażać
		innych swoimi pasjami) oraz w ramach eksperymentu, uwzględniając szczególną specyfikę pracy
		biurowej (którą znam z własnego doświadczenia).
	</p>

	<p>Skoro pracujesz w biurze, to najprawdopobniej masz do dyspozycji:</p>

	<ul>
		<li>biurko,</li>
		<li>fotel/ krzesło,</li>
		<li>komputer/ laptop,</li>
		<li>Internet,</li>
		<li>kubek kawy/ herbaty,</li>
		<li>
			ileś tam czasu pomiędzy wykonywaniem obowiązków zawodowych, które możesz poświęcić na coś
			produktywnego 😉
		</li>
	</ul>

	<p>A ponadto potrafisz:</p>

	<ul>
		<li>siedzieć godzinami przed komputerem,</li>
		<li>stukać palcami w klawiaturę,</li>
		<li>czytać i pisać,</li>
		<li>
			dodawać i odejmować dwucyfrowe liczby oraz mnożyć i dzielić w zakresie tabliczki mnożenia.
		</li>
	</ul>

	<p>
		Jeśli powyższe punkty w większości się zgadzają, gratuluję - najprawdopodobniej uda Ci się
		napisać kilka sensownych linijek kodu i zaczarować swoją przeglądarkę.
	</p>

	<p>
		Celem niniejszej strony jest przede wszystkim pobudzenie Twojej ciekawości i nie zniechęcenie
